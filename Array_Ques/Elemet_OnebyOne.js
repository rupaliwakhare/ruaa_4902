// Print array elements one by one using a loop.

let arr = [2,5,7,8,4]

for (let i = 0; i<arr.length; i++){
    console.log(arr[i]);
    
}

// dryrun
// i = 0   arr[0] = 2   → print 2
// i = 1   arr[1] = 5   → print 5
// i = 2   arr[2] = 7   → print 7
// i = 3   arr[3] = 8   → print 8
// i = 4   arr[4] = 4   → print 4


// output
// 2
// 5
// 7
// 8
// 4